def conf_of_tournament(tournament: str) -> str | None:
    """Confederation a continental competition belongs to, or None for cross-confederation
    competitions (Friendly / World Cup / Confederations Cup — the bridge games)."""
    for conf, kws in CONF_KW.items():
        if any(k in tournament for k in kws):
            return conf
    return None


def apply_offsets(elo_ratings: dict, team_offset: dict = TEAM_OFFSET) -> dict:
    """Return a copy of `elo_ratings` (keyed by Elo-name) with each team's empirical-Bayes
    confederation offset added. Teams not in `team_offset` are returned unchanged."""
    out = dict(elo_ratings)
    for team, off in team_offset.items():
        en = wc2026_teams.elo_name(team)
        if en in out:
            out[en] = out[en] + off
    return out


# ---- calibration (used by scripts/calibrate_confed_offsets.py; not the hot path) ----------

def confederation_map(results) -> dict:
    """team -> confederation, by majority of the team's continental-competition appearances.
    `results` is the raw international-results frame (home_team/away_team/tournament)."""
    from collections import Counter
    votes: dict[str, Counter] = {}
    for r in results.itertuples(index=False):
        c = conf_of_tournament(r.tournament)
        if not c:
            continue
        for tm in (r.home_team, r.away_team):
            votes.setdefault(tm, Counter())[c] += 1
    return {tm: cc.most_common(1)[0][0] for tm, cc in votes.items()}


def cross_counts(calib, team_conf: dict, asof, halflife_years: float = 8.0) -> dict:
    """team -> recency-weighted count of cross-confederation games (the EB anchoring strength)."""
    ch = calib["home_team"].map(team_conf); ca = calib["away_team"].map(team_conf)
    cross = (ch != ca).values & ch.notna().values & ca.notna().values
    age = ((np.datetime64(asof) - calib["date"].values.astype("datetime64[D]"))
           .astype("timedelta64[D]").astype(float) / 365.25)
    rw = 0.5 ** (np.clip(age, 0, None) / halflife_years)
    home = calib["home_team"].values; away = calib["away_team"].values
    out: dict[str, float] = {}
    for k in np.nonzero(cross)[0]:
        out[home[k]] = out.get(home[k], 0.0) + rw[k]
        out[away[k]] = out.get(away[k], 0.0) + rw[k]
    return out


def fit_base_offsets(calib, team_conf: dict, counts: dict, asof, k: float = SHRINK_K,
                     halflife_years: float = 8.0, iters: int = 300, lr: float = 0.6) -> dict:
    """Estimate the per-confederation base offsets under the EB shrinkage weight
    w(team) = k/(n_cross(team)+k), by iteratively zeroing each confederation's
    importance-and-recency-weighted cross-confederation surprise. UEFA pinned to 0."""
    b = calib.copy()
    b["ch"] = b["home_team"].map(team_conf); b["ca"] = b["away_team"].map(team_conf)
    b = b.dropna(subset=["ch", "ca"]); b = b[b["ch"] != b["ca"]]
    age = ((np.datetime64(asof) - b["date"].values.astype("datetime64[D]"))
           .astype("timedelta64[D]").astype(float) / 365.25)
    rw = b["imp"].values * 0.5 ** (np.clip(age, 0, None) / halflife_years)
    dr = b["dr_eff"].values
    w = np.where(b["goal_diff"].values > 0, 1.0, np.where(b["goal_diff"].values == 0, 0.5, 0.0))
    hi = np.array([CONFEDERATIONS.index(c) for c in b["ch"]])
    ai = np.array([CONFEDERATIONS.index(c) for c in b["ca"]])
    sh = np.array([k / (counts.get(t, 0.0) + k) for t in b["home_team"]])
    sa = np.array([k / (counts.get(t, 0.0) + k) for t in b["away_team"]])
    delta = np.zeros(len(CONFEDERATIONS))
    for _ in range(iters):
        dd = delta[hi] * sh - delta[ai] * sa
        we = 1.0 / (1.0 + 10 ** (-(dr + dd) / 400.0))
        deriv = np.log(10) / 400.0 * we * (1 - we)
        grad = np.zeros(len(CONFEDERATIONS)); wsum = np.zeros(len(CONFEDERATIONS))
        np.add.at(grad, hi, rw * sh * (w - we)); np.add.at(wsum, hi, rw * deriv * sh * sh)
        np.add.at(grad, ai, -rw * sa * (w - we)); np.add.at(wsum, ai, rw * deriv * sa * sa)
        step = lr * grad / np.maximum(wsum, 1e-9)
        step[0] = 0.0
        delta += step; delta -= delta[0]
    return dict(zip(CONFEDERATIONS, delta))


def team_offsets(base: dict, counts: dict, conf_map: dict = WC_TEAM_CONF,
                 k: float = SHRINK_K) -> dict:
    """Effective per-team offset = base[conf] * k/(n_cross + k), keyed by canonical team name."""
    out = {}
    for team, conf in conf_map.items():
        en = wc2026_teams.elo_name(team)
        out[team] = base.get(conf, 0.0) * k / (counts.get(en, 0.0) + k)
    return out
