"""Confederation-bias correction for the Elo ratings (empirical-Bayes shrinkage).

Elo is zero-sum: a result only transfers points between the two opponents, so the total
rating *inside* a confederation is conserved regardless of that confederation's true global
level. Confederations are near-disconnected clusters joined by sparse "bridge" games
(inter-confederation friendlies / World Cup matches), so the global scale between clusters
is weakly anchored and drifts — weaker confederations accumulate inflated ratings against
weak regional opponents, and pure Elo cannot re-level them.
(refs: football-rankings.info 2022 "Is there a regional bias in the Elo rating?";
Szczeciński & Roatis 2022 arXiv:2201.00691; Lasek/Szlávik/Bhulai 2013; Bradley-Terry under
group sparsity arXiv:1111.5110; Peeters 2018 on squad value as a global anchor.)

The correction is an empirical-Bayes shrinkage toward a confederation re-leveling prior. Each
confederation has a base offset `delta[conf]` estimated from cross-confederation results only
(UEFA = reference 0). Each TEAM then receives a fraction of its confederation's offset that
shrinks with how well its rating is anchored across confederations:

    offset(team) = delta[conf(team)] * K / (n_cross(team) + K)

where `n_cross(team)` is the recency-weighted (8y half-life) count of the team's
cross-confederation games and `K` is the shrinkage scale. This is the standard EB weight
`tau^2/(tau^2+sigma_i^2)` with `sigma_i^2 ~ 1/n_cross`: a team that plays many intercontinental
games is well-anchored (small offset — trust its own rating); a regionally-isolated team has a
high-variance cross-confederation rating and falls back to its confederation prior (full
offset). Counts, not shares, so a side with 8 of 20 cross games is treated as better-anchored
than one with 2 of 4. (refs: James-Stein / Efron-Morris; empirical-Bayes for paired
comparisons arXiv:1807.09236; Glicko RD as a connectivity-aware uncertainty, Glickman 1999.)

Why apply before the squad-value blend: the globally-comparable squad value then further
corroborates the strong, well-resourced teams, so the correction never over-penalizes them.

Validation (scripts/calibrate_confed_offsets.py): a time-split, cross- vs within-confederation
stratified backtest on ~49k internationals. The shrinkage improves the out-of-sample CROSS-
confederation slice by ~+4.6% RPS (vs ~+3.2% for a flat per-confederation offset), and the
gain over the flat offset is significant after accounting for match dependence (Diebold-Mariano
HAC p≈0.004; team-cluster bootstrap 95% CI excludes 0). The within-confederation slice is
essentially untouched. The per-team offsets below are recomputed by that script.
"""

